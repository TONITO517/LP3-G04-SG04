
public class SumaArreglo {
    public static int sumaArreglo(int[] arr) {
        int suma = 0;
        for (int num : arr) {
            suma += num;
        }
        return suma;
    }


    public static void main(String[] args) {
        int[] numeros = {1, 2, 3, 4, 5};
        System.out.println(sumaArreglo(numeros)); // Salida: 15
    }
}
